import pytest
from a_star import solve


@pytest.mark.parametrize("test_data, result", [
    (("a", "b", {"a": [("b", 1.0)]}), "ab"),
    (('a', 'e', {'a': [('b', 3.0), ('d', 5.0)], 'b': [('c', 1.0)], 'c': [('d', 1.0)], 'd': [('e', 1.0)],
                 'e': []}), 'ade'),
    (('a', 'f', {'a': [('b', 2.3)], 'b': [('c', 4.523)], 'c': [('d', 11.24), ('f', 30.309)], 'd': [('e', 3.3)], 'f': [],
                 'e': [('f', 3.4)]}), "abcdef"),
    (('a', 'z', {'a': [('b', 2.0), ('x', 2.0)], 'b': [('c', 3.0)], 'c': [('z', 4.0)], 'z': [], 'x': [('y', 3.0)],
                 'y': [('z', 4.0)]}), "axyz"),
    (('z', 'a', {'b': [('a', 2.0)], 'a': [], 'c': [('b', 3.0)], 'z': [('c', 4.0), ('y', 4.0)], 'x': [('z', 2.0)],
                 'y': [('x', 3.0)]}), "zcba"),
    (('a', 'z', {'a': [('b', 1.0), ('e', 4.0)], 'b': [('c', 1.0)], 'c': [('z', 1.0)], 'e': [('y', 1.0)],
                 'y': [('z', 1.0)], 'z': []}), 'abcz'),
    (('e', 'a', {'e': [('b', 1.0)], 'b': [('d', 1.0), ('a', 4.0), ('c', 1.0)], 'd': [('e', 1.0), ('a', 1.0)],
                 'a': [('b', 4.0)], 'c': [('a', 1.0)]}), 'ebca')
])
def test_a_start(test_data, result):
    assert solve(*test_data) == result
